package uva.poo.academia;

public class Fecha {
	
	private int dia;
	private int mes;
	private int anyo;
	
	public Fecha(int dia,int mes, int anyo){
		this.dia = dia;
		this.mes = mes;
		this.anyo = anyo;
	}
	
	public int getDia() {
		return dia;
	}
	public int getMes() {
		return mes;
	}
	public int getAnyo() {
		return anyo;
	}
	
	

}
